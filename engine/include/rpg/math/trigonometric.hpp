    template <typename T>
    constexpr T sin(T x) {
        std::size_t lookupSize = trigonometricLookupTable<T>.size() - 1;
        std::size_t steps = lookupSize * 4;
        using ssize_t = std::make_signed_t<std::size_t>;
        auto i = static_cast<ssize_t>(
            (x / (static_cast<T>(2.0) * pi<T>)
        ) * steps) % steps;
        std::size_t j = i % lookupSize;
        if (i < lookupSize) return trigonometricLookupTable<T>[j];
        else if (i < lookupSize * 2) return trigonometricLookupTable<T>[lookupSize - j];
        else if (i < lookupSize * 3) return -trigonometricLookupTable<T>[j];
        else return -trigonometricLookupTable<T>[lookupSize - j];
    }

    template <typename T>
    constexpr T cos(T x) {
        return sin(x + (pi<T> / static_cast<T>(2.0)));
    }

    template <typename T>
    constexpr T tan(T x) {
        return sin(x) / cos(x);
    }

    template <typename T>
    constexpr T ctg(T x) {
        return cos(x) / sin(x);
    }

    template <typename T>
    constexpr T asin(T x) {
        std::size_t lookupSize = trigonometricReverseLookupTable<T>.size() - 1;
        if (x >= 0.0f) {
            std::size_t i = x * lookupSize;
            return trigonometricReverseLookupTable<T>[i];
        } else {
            std::size_t i = -x * lookupSize;
            return -trigonometricReverseLookupTable<T>[i];
        }
    }

    template <typename T>
    constexpr T acos(T x) {
        return asin(-x) + (pi<T> / static_cast<T>(2.0));
    }

    template <typename T>
    constexpr T atan(T x) {
        return asin(x / sqrt(x * x + static_cast<T>(1)));
    }

    template <typename T>
    constexpr T actg(T x) {
        return acos(x / sqrt(x * x + static_cast<T>(1)));
    }

